#include <loki/system.h>
#define PORT 0x3f8

void init_debug()
{
	outb(PORT + 1, 0x00);
	outb(PORT + 3, 0x80);
	outb(PORT + 0, 0x03);
	outb(PORT + 1, 0x00);
	outb(PORT + 3, 0x03);
	outb(PORT + 2, 0xC7);
	outb(PORT + 4, 0x0B);

}

int is_transmit_empty()
{
	return inb(PORT + 5) & 0x20;
}

void write_serial(char a)
{
	while (is_transmit_empty() == 0);

	outb(PORT,a);

}
void serial_puts(const char* data)
{
	size_t datalen = strlen(data);
	for ( size_t i = 0; i < datalen; i++ )
		write_serial(data[i]);
}
